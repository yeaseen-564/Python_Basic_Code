import math
x=2.9
y=3.4
z=5.2
# round always make the float number to integer
print(f"round : {round(x)}")
# abs() always returns positive value:
print(abs(-x))
print(abs(-y))
print(abs(-z))
# use of ceil
print("ceil of x :",math.ceil(x))
print("ceil of y :",math.ceil(y))
print("ceil of z :",math.ceil(z))
# use of floor
print("floor of x :",math.floor(x))
print("floor of y :",math.floor(y))
print("floor of z :",math.floor(z))
