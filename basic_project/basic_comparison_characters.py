weight = float(input("Weight : "))
unit_press = input("(L)bs or (K)g : ")
if unit_press.upper() == "L":
    print(f"You\'re {weight / 0.45} pounds")
elif unit_press.upper() == "K":
    print(f"You\'re {weight / 2.205} kilograms")

